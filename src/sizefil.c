int	sizefil(char *txt)
{
	int	x;
	int	lfil2;

	x = 0;
	lfil2 = 0;
	while (txt[x] != '\0')
	{
		if (txt[x] == 10)
		{
			lfil2 = x;
			break ;
		}
		x++;
	}
	return (lfil2);
}
