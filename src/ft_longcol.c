#include <stdio.h>

int	ft_longcol(char *txt)
{
	int	ki;
	int	ke;

	ki = 0;
	ke = 0;
	while (*(txt + ke) != 0)
	{
		if (*(txt + ke) == '\n')
			ki++;
		ke++;
	}
	return (ki);
}
