int	ft_atoi(char *str)
{
	int	num;

	num = 0;
	while (*str == 32)
		str++;
	while (*str > 48 && *str < 58)
	{
		num *= 10;
		num = num - (*str - 48);
		str++;
	}
	num *= -1;
	return (num);
}
